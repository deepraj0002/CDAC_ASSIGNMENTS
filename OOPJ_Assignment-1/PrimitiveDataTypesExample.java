//1. Write a Java program to declare and initialize all eight primitive data types and print their values
public class PrimitiveDataTypesExample {
    public static void main(String[] args) {
        // Declaration and initialization of all primitive data types
        byte b = 10;
        short s = 1000;
        int i = 500;
        long l = 100000L;
        float f = 5.75f;
        double d = 19.99;
        char c = 'A';
        boolean bool = true;

        // Printing values of each primitive data type
        System.out.println("byte: " + b);
        System.out.println("short: " + s);
        System.out.println("int: " + i);
        System.out.println("long: " + l);
        System.out.println("float: " + f);
        System.out.println("double: " + d);
        System.out.println("char: " + c);
        System.out.println("boolean: " + bool);
    }
}
